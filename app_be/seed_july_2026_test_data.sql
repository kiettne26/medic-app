-- --------------------------------------------
-- Generate approved slots from doctor schedules.
-- --------------------------------------------
WITH dates AS (
    SELECT gs::DATE AS work_date
    FROM generate_series(DATE '2026-07-01', DATE '2026-07-10', INTERVAL '1 day') AS gs
),
generated_slots AS (
    SELECT
        uuid_generate_v5(
            uuid_ns_url(),
            'medic-app:july2026:slot:' || s.id::TEXT || ':' || d.work_date::TEXT || ':' || to_char(slot_start, 'HH24:MI')
        ) AS id,
        s.doctor_id,
        s.id AS schedule_id,
        d.work_date AS slot_date,
        slot_start::TIME AS start_time,
        (slot_start + make_interval(mins => s.slot_duration_minutes))::TIME AS end_time
    FROM doctor_schedules s
    JOIN dates d
      ON EXTRACT(DOW FROM d.work_date)::INT = s.day_of_week
    CROSS JOIN LATERAL generate_series(
        d.work_date::TIMESTAMP + s.start_time,
        d.work_date::TIMESTAMP + s.end_time - make_interval(mins => s.slot_duration_minutes),
        make_interval(mins => s.slot_duration_minutes)
    ) AS slot_start
    WHERE s.doctor_id IN (SELECT id FROM _july_test_doctors)
      AND s.is_active = true
)
